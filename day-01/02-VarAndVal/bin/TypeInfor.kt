fun main(args: Array<String>) {
	val aByte:Byte = Byte.MAX_VALUE
	val bByte:Byte = Byte.MIN_VALUE
	
	println(aByte)
	println(bByte)
	
	val cInt:Int = Int.MAX_VALUE
	val dInt:Int = Int.MIN_VALUE
	
	println(cInt)
	println(dInt)
	
	val eLong:Long = Long.MAX_VALUE
	val fLong:Long = Long.MIN_VALUE
	
	println(eLong)
	println(fLong)
	
	var gInt:Int = 0b0011
	println(gInt)
}